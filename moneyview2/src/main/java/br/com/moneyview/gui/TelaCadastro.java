    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        String nome = txtNomeUsuario.getText();
        String email = txtEmail.getText();
        String senha = txtSenha.getText();
        
        UsuarioServicos usuarioServicos = new UsuarioServicos();
        boolean registrado = usuarioServicos.registrarUsuario(nome, email, senha);
        
        if (!registrado) {
            JOptionPane.showMessageDialog (null, "cadastro falhou, ou usuário ja está cadastrado", "Erro!", JOptionPane.ERROR_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        }
        
        TelaLogin login = new TelaLogin();
        login.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnCadastroActionPerformed

